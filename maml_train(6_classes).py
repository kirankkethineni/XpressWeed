def rgb_to_class(mask_img_pil):

    """Converts an RGB PIL Image mask to a 2D integer class mask."""
    mask_array_np = np.array(mask_img_pil)
    if mask_array_np.ndim == 2:
        return mask_array_np.astype(np.int32)
    if mask_array_np.shape[-1] != 3:
        return np.zeros(mask_array_np.shape[:2], dtype=np.int32)
    class_mask_np = np.zeros(mask_array_np.shape[:2], dtype=np.int32)
    for color, class_idx in color2class.items():
        match = np.all(mask_array_np == np.array(color).reshape(1, 1, 3), axis=-1)
        class_mask_np[match] = class_idx
    return class_mask_np

