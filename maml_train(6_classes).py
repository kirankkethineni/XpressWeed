import tensorflow as tf


def make_weighted_categorical_ce(class_weights,
                                 ignore_val=None,
                                 cap_bg=True):
    """
    Weighted CE that works for 2-D or 3-/4-D masks.
    """
    class_w = tf.constant(class_weights, dtype=tf.float32)

    # Optional: cap background (class 0) weight
    if cap_bg:
        class_w = tf.tensor_scatter_nd_update(class_w,
                                              indices=[[0]],
                                              updates=[tf.minimum(class_w[0], 1.0)])

    def loss_fn(y_true, y_pred):
        # -------- 1.  Standardise ground-truth shape ----------------------
        y_true = tf.cast(y_true, tf.int32)
        if y_true.shape.rank is not None and y_true.shape.rank > 0 and y_true.shape[-1] == 1:
            y_flat = tf.squeeze(y_true, axis=-1)   # (… , H , W)
        else:
            y_flat = y_true                        # already (… , H , W)

        # -------- 2.  Valid-pixel mask -----------------------------------
        if ignore_val is not None:
            valid = tf.not_equal(y_flat, ignore_val)           # bool same shape as y_flat
            y_clean = tf.where(valid, y_flat, tf.zeros_like(y_flat))
        else:
            valid  = tf.ones_like(y_flat, tf.bool)
            y_clean = y_flat

        # -------- 3.  One-hot encode GT ----------------------------------
        y_onehot = tf.one_hot(y_clean, depth=len(class_weights))  # shape (… , H , W , C)

        # -------- 4.  Clip predictions for stability --------------------
        y_pred = tf.clip_by_value(y_pred, 1e-7, 1.0 - 1e-7)

        # -------- 5.  Per-pixel CE * weights ----------------------------
        ce   = -tf.reduce_sum(y_onehot * tf.math.log(y_pred), axis=-1)      # (… , H , W)
        wts  = tf.reduce_sum(class_w * y_onehot,           axis=-1)         # (… , H , W)
        per_px = ce * wts                                                  # (… , H , W)

        per_px = tf.where(valid, per_px, 0.0)

        # -------- 6.  Normalise by #valid pixels ------------------------
        denom = tf.maximum(tf.reduce_sum(tf.cast(valid, tf.float32)), 1.0)
        return tf.reduce_sum(per_px) / denom

    return loss_fn



