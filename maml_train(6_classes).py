def accumulate_dataset_scores(model, pairs, target_size=TARGET_SIZE, ignore_val=IGNORE_VAL):
    y_true_per_class, y_score_per_class, classes_seen = {}, {}, set()

    for img_path, mask_path in pairs:
        img_arr, gt = _load_img_mask(img_path, mask_path, target_size)
        probs = predict_probs(model, img_arr)  # (H,W,C)
        H, W, C = probs.shape

        valid = (gt != ignore_val) if (ignore_val is not None) else np.ones_like(gt, dtype=bool)
        gt_flat = gt[valid].ravel()

        for c in range(C):
            classes_seen.add(c)
            s_flat = probs[..., c][valid].ravel()
            y_bin  = (gt_flat == c)

            if c not in y_true_per_class:
                y_true_per_class[c]  = y_bin
                y_score_per_class[c] = s_flat
            else:
                y_true_per_class[c]  = np.concatenate([y_true_per_class[c],  y_bin])
                y_score_per_class[c] = np.concatenate([y_score_per_class[c], s_flat])

    classes = sorted(list(classes_seen))
    return y_true_per_class, y_score_per_class, classes

def plot_pr_curves(y_true_per_class, y_score_per_class, classes, class_names=None, title="Precision–Recall (dataset)"):
    plt.figure(figsize=(8, 6))
    aps = []

    # micro-average across all classes
    y_true_micro  = np.concatenate([y_true_per_class[c] for c in classes])
    y_score_micro = np.concatenate([y_score_per_class[c] for c in classes])
    prec_micro, rec_micro, _ = precision_recall_curve(y_true_micro, y_score_micro)
    ap_micro = average_precision_score(y_true_micro, y_score_micro)

    # per-class curves
    for c in classes:
        yt = y_true_per_class[c]; ys = y_score_per_class[c]
        ap = average_precision_score(yt, ys) if yt.sum() > 0 else 0.0
        aps.append(ap)
        name = class_names.get(c, f"Class {c}") if class_names else f"Class {c}"
        prec, rec, _ = precision_recall_curve(yt, ys)
        plt.plot(rec, prec, linewidth=2, label=f"{name} (AP={ap:.3f})")

    mAP = float(np.mean(aps)) if len(aps) else 0.0
    plt.plot(rec_micro, prec_micro, linewidth=2, linestyle="--", label=f"micro (AUPRC={ap_micro:.3f})")
    plt.xlabel("Recall"); plt.ylabel("Precision")
    plt.title(f"{title}\nmacro mAP={mAP:.3f} | micro AUPRC={ap_micro:.3f}")
    plt.legend(loc="lower left", fontsize=9); plt.grid(True, alpha=0.3); plt.tight_layout(); plt.show()

    print("\n— Average Precision (AP) per class —")
    for c, ap in zip(classes, aps):
        name = class_names.get(c, f"Class {c}") if class_names else f"Class {c}"
        print(f"  {name:<28} AP = {ap:.4f}")
    print(f"\nmacro mAP = {mAP:.4f}")
    print(f"micro AUPRC = {ap_micro:.4f}")

