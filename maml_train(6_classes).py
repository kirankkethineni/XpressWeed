# ------------------------------------------------------------------
def combined_loss_fn_factory(class_weights,
                             ignore_val=None):
    """Creates a loss that auto-switches on channel count."""
    cat_loss = make_weighted_categorical_ce(class_weights, ignore_val)

    def loss(y_true, y_pred):
        # If model outputs 1 channel → fall back to binary CE
        if y_pred.shape[-1] == 1:
            # legacy binary branch kept for safety
            y_true_f = tf.cast(y_true, tf.float32)
            y_pred_f = tf.nn.sigmoid(y_pred)
            return tf.reduce_mean(
                tf.keras.losses.binary_crossentropy(y_true_f, y_pred_f))
        else:
            # normal multiclass path (used in our new setup)
            return cat_loss(y_true, y_pred)

    return loss

