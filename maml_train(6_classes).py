def load_images_and_masks(
        pair_list,
        target_size=(512, 768),
        is_support=False,
        union_foreground=True,      # True  ➜ binary union mask (0 / 1)
                                    # False ➜ class-aware mask (0 / cls_id)
        ignore_val=None             # e.g. 255 for “void / unlabeled”
    ):
    """
    Returns two numpy arrays:
        xs – float32 images in [0, 1], shape (N, H, W, 3)
        ys – int32 masks,          shape (N, H, W, 1)

    Support tuples : (img_path, mask_path, class_id)
    Query   tuples : (img_path, mask_path)

    Support-mask logic
    ------------------
    union_foreground = True
        → mask = 1  where pixel belongs to *any* episode class
          mask = 0  elsewhere  (classic binary foreground / background)

    union_foreground = False
        → mask = cls_id  where pixel belongs to *its* class
          mask = 0       for all other pixels
          (class-aware single-class mask, reduces inter-class confusion)
    """
    xs, ys = [], []

    # ---------- collect all class IDs present in this episode ----------
    fg_ids_episode = {tup[2] for tup in pair_list} if is_support else set()

    for item in pair_list:
        try:
            # --------------- unpack tuple ---------------
            if is_support:
                if len(item) != 3:
                    print(f"Skipping malformed support item: {item}")
                    continue
                img_path, mask_path, cls_id = item
            else:
                if len(item) != 2:
                    print(f"Skipping malformed query item: {item}")
                    continue
                img_path, mask_path = item
                cls_id = None  # not used for query

            # --------------- sanity-check paths ---------------
            if not (os.path.isfile(img_path) and os.path.isfile(mask_path)):
                print(f"Skipping (file not found): {img_path} | {mask_path}")
                continue

            # --------------- load & scale image ---------------
            img_arr = img_to_array(
                load_img(img_path,
                         target_size=target_size,
                         color_mode="rgb")
            ) / 255.0  # float32 in [0, 1]

            # --------------- load mask (H, W, 1) ---------------
            mask_arr = img_to_array(
                load_img(mask_path,
                         target_size=target_size,
                         color_mode="grayscale")
            ).astype(np.int32)

            # --------------- support-mask transformation ---------------
            if is_support:
                if union_foreground:
                    # binary union: 1 if pixel ∈ any class in episode
                    mask_mod = np.isin(mask_arr,
                                       list(fg_ids_episode)).astype(np.uint8)
                else:
                    # class-aware single-class mask: 0 / cls_id
                    mask_mod = np.where(mask_arr == cls_id,
                                        cls_id,
                                        0).astype(np.uint8)

                if ignore_val is not None:
                    mask_mod[mask_arr == ignore_val] = ignore_val

                mask_arr = mask_mod  # overwrite with transformed mask

            # --------------- accumulate ---------------
            xs.append(img_arr)
            ys.append(mask_arr)

        except Exception as e:
            print(f"Skipping invalid pair {item}: {e}")
            continue

    # ---------- empty fallback ----------
    if not xs:
        empty_x = np.empty((0, target_size[0], target_size[1], 3),
                           dtype=np.float32)
        empty_y = np.empty((0, target_size[0], target_size[1], 1),
                           dtype=np.int32)
        return empty_x, empty_y

    return np.array(xs, dtype=np.float32), np.array(ys, dtype=np.int32)

