def build_per_class_images(
        image_dir,
        mask_dir,
        NUM_CLASSES_MAML=6,
        ignore_val=255,        # set to None if your dataset has no “void”
        min_pairs_rare=4,      # ≤ this many images → rare class
        verbose=True
    ):
    """
    Scans `image_dir` / `mask_dir` and returns:
        per_class_images  : {class_id: [(img_path, mask_path), ...]}
        rare_classes      : [class_ids with few samples]
        class_counts      : Counter({class_id: #images_containing_it})
        unique_pairs_list : list of unique (img_path, mask_path) tuples

    Notes
    -----
    • Each mask is assumed to be *grayscale* with integer values 0‥N.
    • Any pixel equal to `ignore_val` is skipped.
    • A single image–mask pair may be added to multiple class lists if the
      mask contains several classes.
    """
    per_class_images = defaultdict(list)
    all_class_labels_for_counting = []
    unique_pairs_hash = set()          # to avoid duplicates
    unique_pairs_list = []

    # ---------- basic sanity checks ----------
    if not os.path.isdir(image_dir):
        raise FileNotFoundError(f"Image dir not found: {image_dir}")
    if not os.path.isdir(mask_dir):
        raise FileNotFoundError(f"Mask dir not found:  {mask_dir}")

    mask_files = [f for f in os.listdir(mask_dir)
                  if f.lower().endswith((".png", ".jpg", ".jpeg"))]
    if not mask_files:
        raise RuntimeError(f"No mask files found in {mask_dir}")

    # ---------- iterate over all masks ----------
    for fname in mask_files:
        mask_path = os.path.join(mask_dir, fname)
        img_base  = os.path.splitext(fname)[0]

        # try to find a sibling image (JPEG or PNG)
        img_path = next((
            os.path.join(image_dir, img_base + ext)
            for ext in (".jpeg", ".jpg", ".png")
            if os.path.isfile(os.path.join(image_dir, img_base + ext))
        ), None)
        if img_path is None:
            continue  # skip if image missing

        # avoid double-adding the exact same pair
        pair_hash = hashlib.md5(f"{img_path}|{mask_path}".encode()).hexdigest()
        if pair_hash in unique_pairs_hash:
            continue
        unique_pairs_hash.add(pair_hash)
        unique_pairs_list.append((img_path, mask_path))

        # load mask (grayscale) ➜ (H, W)
        mask_arr = img_to_array(load_img(mask_path,
                                         color_mode="grayscale")).astype(np.uint8)
        mask_arr = np.squeeze(mask_arr, axis=-1)

        # filter out ignore label
        if ignore_val is not None:
            mask_arr = mask_arr[mask_arr != ignore_val]

        unique_classes = np.unique(mask_arr)
        for cls in unique_classes:
            if 0 <= cls < NUM_CLASSES_MAML:
                per_class_images[int(cls)].append((img_path, mask_path))
                all_class_labels_for_counting.append(int(cls))

    # ---------- ensure every class key exists ----------
    for cls_id in range(NUM_CLASSES_MAML):
        per_class_images.setdefault(cls_id, [])

    class_counts = Counter(all_class_labels_for_counting)
    rare_classes = [c for c, imgs in per_class_images.items()
                    if len(imgs) <= min_pairs_rare]

    # ---------- logging ----------
    if verbose:
        print(f"✅ Total unique image–mask pairs: {len(unique_pairs_list)}")
        for cls_id in range(NUM_CLASSES_MAML):
            n = len(per_class_images[cls_id])
            tag = " (RARE)" if cls_id in rare_classes else ""
            print(f"  Class {cls_id}: {n:>4} images{tag}")

    return per_class_images, rare_classes, class_counts, unique_pairs_list

