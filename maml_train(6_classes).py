def evaluate_prediction_metrics(pred_mask, gt_mask):
    """
    Calculates various pixel-wise segmentation metrics.

    Inputs:
        pred_mask: (H,W) array of predicted class-IDs
        gt_mask:   (H,W) array of ground-truth class-IDs

    Returns:
        A dictionary containing:
          - overall_accuracy (float)
          - ground_truth_counts (Counter)
          - prediction_counts (Counter)
          - per_class_accuracy (dict {int: float or None})
    """
    if pred_mask.shape != gt_mask.shape:
        print("Warning: Predicted and Ground Truth mask shapes do not match.")
        # Decide how to handle this - for now, raise an error or return empty metrics
        # raise ValueError("Mask shapes must match")
        return {
            'overall_accuracy': 0.0,
            'ground_truth_counts': Counter(),
            'prediction_counts': Counter(),
            'per_class_accuracy': {}
        }


    # Calculate overall accuracy
    correct_pixels = np.sum(pred_mask == gt_mask)
    total_pixels = gt_mask.size
    overall_accuracy = correct_pixels / total_pixels if total_pixels > 0 else 0.0

    # Calculate pixel counts for GT and Prediction
    ground_truth_counts = Counter(gt_mask.flatten())
    prediction_counts = Counter(pred_mask.flatten())

    # Calculate per-class accuracy
    per_class_accuracy = {}
    all_classes = sorted(set(ground_truth_counts.keys()) | set(prediction_counts.keys()))

    for cls in all_classes:
        # Pixels belonging to this class in the ground truth
        gt_class_mask = (gt_mask == cls)
        num_gt_pixels = np.sum(gt_class_mask)

        if num_gt_pixels == 0:
            # If class is not present in GT, accuracy is undefined or 1.0 (no incorrect predictions possible *for this class*)
            # Returning None is often clearer to indicate lack of GT presence
            per_class_accuracy[cls] = None
        else:
            # Pixels correctly predicted as this class
            correctly_predicted_as_class = np.sum((pred_mask == cls) & gt_class_mask)
            per_class_accuracy[cls] = correctly_predicted_as_class / num_gt_pixels

    return {
        'overall_accuracy': overall_accuracy,
        'ground_truth_counts': ground_truth_counts,
        'prediction_counts': prediction_counts,
        'per_class_accuracy': per_class_accuracy
    }

