def parse_json_boxes(json_path, target_size):
    """
    Converts normalized YOLO-style boxes to absolute pixel boxes.
    target_size = (orig_h, orig_w)
    """
    with open(json_path, 'r') as f:
        boxes = json.load(f)

    abs_boxes = []
    orig_h, orig_w = target_size

    for obj in boxes:
        cid = int(obj["class_id"])
        xc, yc = obj["x_center"] * orig_w, obj["y_center"] * orig_h
        w, h = obj["width"] * orig_w, obj["height"] * orig_h
        x1, y1 = int(xc - w / 2), int(yc - h / 2)
        x2, y2 = int(xc + w / 2), int(yc + h / 2)
        abs_boxes.append((x1, y1, x2, y2, cid))

    return abs_boxes



