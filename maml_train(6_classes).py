# ------------------------------------------------- main routine ---
def build_panels(model, img_path, norm_mode='scale'):
    """
    1. Load original image
    2. Draw bounding boxes (before resize)
    3. Resize both raw image and boxed image
    4. Run model → get segmentation prediction
    5. Return all 3 panels: [raw, boxed, seg_rgb]
    """
    # ---------- Load original full-size image ----------
    img_orig = Image.open(img_path).convert("RGB")
    orig_w, orig_h = img_orig.size
    img_raw = np.asarray(img_orig, np.uint8)

    # ---------- Get JSON path & parse boxes ----------
    stem = os.path.splitext(os.path.basename(img_path))[0]
    json_path = os.path.join(JSON_DIR, stem + JSON_EXT)

    if not os.path.isfile(json_path):
        print("⚠️ JSON not found:", json_path)
        boxes = []
    else:
        boxes = parse_json_boxes(json_path, target_size=(orig_h, orig_w))  # Pass original size

    # ---------- Draw boxes on original image ----------
    img_with_boxes = draw_boxes(img_raw, boxes)

    # ---------- Resize both raw and boxed ----------
    img_resized        = Image.fromarray(img_raw).resize(TARGET_SZ[::-1], Image.BILINEAR)
    img_boxes_resized  = Image.fromarray(img_with_boxes).resize(TARGET_SZ[::-1], Image.BILINEAR)

    # ---------- Prepare model input ----------
    img_arr = np.asarray(img_resized, np.float32)

    if norm_mode == 'scale':
        img_arr /= 255.0
    elif norm_mode == 'tanh':
        img_arr = (img_arr - 127.5) / 127.5
    elif norm_mode == 'imagenet':
        mean = np.array([123.68, 116.78, 103.94])
        std = np.array([58.393, 57.12, 57.375])
        img_arr = (img_arr - mean) / std
    else:
        raise ValueError(f"Unknown norm_mode: {norm_mode}")

    # ---------- Model prediction ----------
    logits = model.predict(img_arr[None, ...], verbose=0)[0]
    pred_mask = np.argmax(logits, -1) if logits.ndim == 3 else (logits > 0.5).astype(np.uint8)
    seg_rgb = colourise(pred_mask)

    print(f"\n🖼️  {os.path.basename(img_path)}")
    print("  Input shape to model:", img_arr.shape)
    print("  Logits shape:", logits.shape, "range:", logits.min(), "→", logits.max())
    print("  Predicted classes:", np.unique(pred_mask))

    return [np.asarray(img_resized, np.uint8), np.asarray(img_boxes_resized, np.uint8), seg_rgb]


