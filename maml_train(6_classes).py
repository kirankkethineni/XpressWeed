def evaluate_predictions(y_true, y_pred):
    """
    Evaluates segmentation predictions against true labels.

    Args:
        y_true (np.ndarray): Ground truth masks (H, W) or (H, W, 1).
        y_pred (np.ndarray): Predicted class masks (H, W).

    Returns:
        dict: A dictionary containing evaluation metrics.
    """
    if y_true.ndim == 3 and y_true.shape[-1] == 1:
        y_true = np.squeeze(y_true, axis=-1)

    # Ensure both are integer types
    y_true = y_true.astype(np.int32)
