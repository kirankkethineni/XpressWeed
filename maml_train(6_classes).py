def update_iou_batch(y_true_int, y_pred_int):
    # y_true_int, y_pred_int: (N, H, W) int arrays
    # ignore pixels
    valid = (y_true_int != IGNORE_VAL)
    for c in range(NUM_CLASSES):
        pred_c = (y_pred_int == c)
        true_c = (y_true_int == c)
        inter  = np.logical_and(pred_c, true_c) & valid
        uni    = (np.logical_or(pred_c, true_c)) & valid
        intersections[c] += inter.sum()
        unions[c]        += uni.sum()

