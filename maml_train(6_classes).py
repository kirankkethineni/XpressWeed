def augment_images_and_masks(images_batch, masks_batch,
                             target_size=(512, 768),
                             rsh_prob=0.85):
    """
    Augments a batch of (images, masks).
    • Images returned as float32 in [0,1]
    • Masks returned as uint8 with original class IDs
    """
    H, W = target_size
    aug_imgs, aug_masks = [], []

    for img, mask in zip(images_batch, masks_batch):

        # --- ensure NumPy arrays ------------------------------------------
        img_np  = np.array(img)   # works for tf.Tensor or np.ndarray
        mask_np = np.array(mask)

        if img_np.max() > 1.0:        # convert from 0–255 if needed
            img_np = img_np / 255.0

        # --- 1) Random left/right flip ------------------------------------
        if np.random.rand() < 0.5:
            img_np  = np.flip(img_np, 1)
            mask_np = np.flip(mask_np, 1)

        # --- 2) Random light zoom (0.9–1.1×) ------------------------------
        # We **resize** because zoom changes spatial resolution.
        scale = np.random.uniform(0.9, 1.1)
        if scale != 1.0:
            new_h, new_w = int(H * scale), int(W * scale)

            # Guard: only resize if both dims > 0
            if img_np.shape[0] > 0 and img_np.shape[1] > 0:
                img_rs = cv2.resize(img_np,  (new_w, new_h),
                                    interpolation=cv2.INTER_LINEAR)
            else:
                img_rs = img_np

            if mask_np.shape[0] > 0 and mask_np.shape[1] > 0:
                mask_rs = cv2.resize(mask_np, (new_w, new_h),
                                     interpolation=cv2.INTER_NEAREST)
            else:
                mask_rs = mask_np

            if scale > 1.0:                       # zoom-in → crop centre
                top  = (new_h - H) // 2
                left = (new_w - W) // 2
                img_np  = img_rs [top:top+H, left:left+W]
                mask_np = mask_rs[top:top+H, left:left+W]
            else:                                 # zoom-out → pad
                pad_h = (H - new_h) // 2
                pad_w = (W - new_w) // 2
                img_np  = np.pad(img_rs,
                                 ((pad_h, H-new_h-pad_h),
                                  (pad_w, W-new_w-pad_w), (0,0)),
                                 mode='reflect')
                mask_np = np.pad(mask_rs,
                                 ((pad_h, H-new_h-pad_h),
                                  (pad_w, W-new_w-pad_w)),
                                 mode='constant', constant_values=0)

        # --- 3) Random shadows / highlights -------------------------------
        if np.random.rand() < rsh_prob:
            img_np = apply_rsh(img_np)

        # --- 4) Global colour jitter --------------------------------------
        img_tf = tf.convert_to_tensor(img_np, tf.float32)
        img_tf = tf.image.random_brightness(img_tf, 0.08)
        img_tf = tf.image.random_contrast(img_tf, 0.9, 1.1)
        img_tf = tf.image.random_saturation(img_tf, 0.9, 1.1)
        img_tf = tf.image.random_hue(img_tf, 0.02)
        img_np = img_tf.numpy()

        # --- 5) Occasional Gaussian blur ----------------------------------
        if np.random.rand() < 0.2:
            sigma = np.random.uniform(0.2, 0.8)
            ksz   = int(2 * math.ceil(3 * sigma) + 1)
            img_np = cv2.GaussianBlur(img_np, (ksz, ksz), sigmaX=sigma)

        # --- 6) Low-level Gaussian noise ----------------------------------
        img_np = np.clip(img_np +
                         np.random.normal(0, 0.01, img_np.shape).astype(np.float32),
                         0.0, 1.0)

        aug_imgs.append(tf.convert_to_tensor(img_np,  dtype=tf.float32))
        aug_masks.append(tf.convert_to_tensor(mask_np, dtype=tf.uint8))

    return tf.stack(aug_imgs), tf.stack(aug_masks)

