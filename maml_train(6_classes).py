def infer_and_colorize(model,
                       img_path,
                       mask_path=None,
                       target_size=(512, 768),
                       palette=CLASS2COLOR):
    """
    Returns:
        img_rgb   – uint8 RGB of the (resized) input
        pred_mask – uint8 (H,W) class-ID prediction
        pred_rgb  – uint8 (H,W,3) colourised prediction
        gt_rgb    – uint8 (H,W,3) colourised GT   (None if mask_path is None)
    """
    # --- load & resize image ---
    img = Image.open(img_path).convert("RGB").resize(target_size[::-1], Image.BILINEAR)
    img_arr = np.asarray(img, np.float32) / 255.0

    # --- run model ---
    logits = model.predict(img_arr[None, ...], verbose=0)[0]
    pred_mask = np.argmax(logits, -1).astype(np.uint8) if logits.ndim == 3 else (logits > 0.5).astype(np.uint8)

    # --- colouriser helper ---
    def colourise(mask):
        colour = np.zeros((*mask.shape, 3), np.uint8)
        for cid, rgb in palette.items():
            colour[mask == cid] = rgb
        return colour

    pred_rgb = colourise(pred_mask)

    # --- optional ground truth ---
    gt_rgb = None
    if mask_path:
        gt_mask = Image.open(mask_path).convert("L").resize(target_size[::-1], Image.NEAREST)
        gt_rgb  = colourise(np.asarray(gt_mask, np.uint8))

    return (np.asarray(img, np.uint8),
            pred_mask,
            pred_rgb,
            gt_rgb)

