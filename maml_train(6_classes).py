def _softmax(x, axis=-1):
    x = x - np.max(x, axis=axis, keepdims=True)
    e = np.exp(x)
    return e / np.sum(e, axis=axis, keepdims=True)

def _load_img_mask(img_path, mask_path, target_size):
    img = Image.open(img_path).convert("RGB").resize(target_size[::-1], Image.BILINEAR)
    img_arr = np.asarray(img, np.float32) / 255.0
    gt = Image.open(mask_path).convert("L").resize(target_size[::-1], Image.NEAREST)
    gt = np.asarray(gt, np.int32)
    return img_arr, gt

def predict_probs(model, img_arr):
    """Return per-class probabilities (H, W, C)."""
    pred = model.predict(img_arr[None, ...], verbose=0)[0]
    if pred.ndim == 3 and pred.shape[-1] > 1:
        # If not normalized, treat as logits
        if not np.allclose(np.sum(np.maximum(pred, 0), axis=-1).mean(), 1.0, atol=1e-3):
            probs = _softmax(pred, axis=-1)
        else:
            probs = pred
    else:
        # Binary head → make [1-p, p]
        p = pred[..., 0] if (pred.ndim == 3 and pred.shape[-1] == 1) else pred
        p = 1 / (1 + np.exp(-p))  # safe sigmoid if logits
        probs = np.stack([1.0 - p, p], axis=-1)
    return probs

