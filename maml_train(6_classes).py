def apply_rsh(image, num_regions=2,
              shadow_intensity=0.5, highlight_intensity=1.5):
    """Overlay random triangular regions as darker/lighter patches."""
    h, w = image.shape[:2]
    mask = np.ones((h, w), dtype=np.float32)

    for _ in range(num_regions):
        pts = np.random.randint(0, [w, h], size=(3, 2)).astype(np.int32)
        intensity = shadow_intensity if np.random.rand() < 0.5 else highlight_intensity
        cv2.fillPoly(mask, [pts], float(intensity))

    return np.clip(image * mask[..., None], 0.0, 1.0).astype(np.float32)

