def _stem(p):
    return os.path.splitext(os.path.basename(p))[0]

def discover_pairs(img_dir, mask_dir, img_exts=(".png",".jpg",".jpeg"), mask_exts=(".png",".jpg",".jpeg"), limit=None):
    imgs = []
    for ext in img_exts:
        imgs += glob.glob(os.path.join(img_dir, f"*{ext}"))
    masks = []
    for ext in mask_exts:
        masks += glob.glob(os.path.join(mask_dir, f"*{ext}"))

    img_map  = { _stem(p): p for p in imgs }
    mask_map = { _stem(p): p for p in masks }
    common = sorted(set(img_map.keys()) & set(mask_map.keys()))
    if limit is not None:
        common = common[:limit]
    pairs = [(img_map[s], mask_map[s]) for s in common]
    if not pairs:
        raise RuntimeError("No image–mask pairs found. Check folder paths & filenames.")
    print(f"Found {len(pairs)} pairs.")
    return pairs

