def get_class_weights_for_maml(class_counts,
                               num_maml_classes,
                               beta=0.999,
                               min_w=0.5,
                               max_w=7.0):
    """
    Balanced class weights using effective number of samples.
    Capped between min_w and max_w, with optional normalization.
    """
    weights = np.ones(num_maml_classes, dtype=np.float32)
    total   = sum(class_counts.get(c, 0) for c in range(num_maml_classes))
    if total == 0:
        return weights.tolist()

    unclipped_weights = []

    for c in range(num_maml_classes):
        n = class_counts.get(c, 0)
        if n == 0:
            w = max_w
        else:
            eff = (1.0 - beta ** n) / (1.0 - beta)
            w = total / eff
        unclipped_weights.append(w)
        weights[c] = np.clip(w, min_w, max_w)

    print(f"\n🔍 Raw effective-number weights (before clipping): {np.round(unclipped_weights, 2)}")
    print(f"🔧 Clipped weights: {np.round(weights, 2)}")

    # Only normalize if there's real variation
    if np.std(weights) > 1e-3:
        weights /= weights.mean()
        print(f"🎯 Normalized weights (mean=1): {np.round(weights, 2)}")
    else:
        print("⚠️ Skipping normalization (weights are uniform)")

    return weights.tolist()

import random

def sample_episode(per_class_images, rare_classes,
                              k_shot_common=10, k_shot_rare=1,
                              n_query_common=10, n_query_rare=2,
                              n_way=3, rare_prob=0.0):
    """
    Samples a MAML episode for segmentation with per-class adaptation.

    Returns:
        episode_tasks : list of tuples:
            (class_id, support_pairs, query_pairs)
            where:
              • support_pairs: [(img_path, mask_path, class_id)]
              • query_pairs  : [(img_path, mask_path)]
    """
    episode_tasks = []

    min_common = k_shot_common + n_query_common
    min_rare   = k_shot_rare   + n_query_rare

    # Filter classes with enough examples
    available_classes = [
        c for c, items in per_class_images.items()
        if c != 0 and len(items) >= (min_rare if c in rare_classes else min_common)
    ]

    if len(available_classes) < n_way:
        return []

    # Separate rare vs. common
    eligible_rare   = [c for c in rare_classes if c in available_classes]
    eligible_common = [c for c in available_classes if c not in eligible_rare]

    selected_classes = []
    random.shuffle(available_classes)

    for _ in range(n_way):
        cls = -1
        if eligible_rare and random.random() < rare_prob:
            choices = [c for c in eligible_rare if c not in selected_classes]
            if choices:
                cls = random.choice(choices)
        if cls == -1:
            choices = [c for c in eligible_common if c not in selected_classes]
            if choices:
                cls = random.choice(choices)
        if cls == -1:
            remaining = [c for c in available_classes if c not in selected_classes]
            if remaining:
                cls = random.choice(remaining)
        if cls != -1:
            selected_classes.append(cls)

    # Sample for each selected class individually
    for cls in selected_classes:
        items = per_class_images[cls]
        is_rare = cls in rare_classes
        k_shot  = k_shot_rare if is_rare else k_shot_common
        n_query = n_query_rare if is_rare else n_query_common
        total   = k_shot + n_query

        chosen = random.sample(items, total) if len(items) >= total \
                 else random.choices(items, k=total)

        support_pairs = [(img, mask, cls) for img, mask in chosen[:k_shot]]
        query_pairs   = chosen[k_shot:k_shot + n_query]

        episode_tasks.append((cls, support_pairs, query_pairs))

    return episode_tasks

def maml_inner_update(
        model,
        support_x,
        support_y,
        init_weights_vals,
        lr,
        loss_fn,
        n_inner,
        cls_id: int = None          # <— NEW: pass class-id for clearer logs
    ):
    """Per-class inner-loop adaptation.

    Returns:
        adapted_weights_vals : list[np.ndarray]  – weights after n_inner steps
        final_support_loss   : tf.Tensor(float) – loss at last inner step
    """
    # ── start from meta-weights ─────────────────────────────────────────
    cur_w_vals = [tf.identity(w) for w in init_weights_vals]

    for step in range(n_inner):
        # load the snapshot into the model
        for var, w_val in zip(model.trainable_weights, cur_w_vals):
            var.assign(w_val)

        # forward / loss / grads
        with tf.GradientTape() as tape:
            logits = model(support_x, training=True)
            loss   = loss_fn(support_y, logits)

        grads = tape.gradient(loss, model.trainable_weights)
        grads = [tf.clip_by_value(g, -1.0, 1.0) if g is not None else None
                 for g in grads]

        # SGD update in Python space
        cur_w_vals = [
            w - lr * g if g is not None else w
            for w, g in zip(cur_w_vals, grads)
        ]

        # optional debug
        if cls_id is not None and step == n_inner - 1:
            print(f"[Inner] cls {cls_id}: step {step+1}/{n_inner}, "
                  f"support loss = {loss.numpy():.4f}")

    return cur_w_vals, loss  # loss from last step

def maml_outer_step(
    model,
    per_class_images, rare_classes,
    meta_lr, inner_lr,
    k_shot_common, k_shot_rare,
    n_query_common, n_query_rare,
    n_way, n_inner, n_tasks,
    combined_loss_fn,
    rare_prob,
    augment,
    current_maml_epoch,
    *,
    union_foreground=False,
    ignore_val=None
):
    """Performs one meta-update step with class-wise adaptation."""
    meta_optimizer = tf.keras.optimizers.Adam(learning_rate=meta_lr)

    accumulated_meta_loss    = 0.0
    accumulated_support_loss = 0.0

    model_train_vars       = model.trainable_weights
    init_meta_weights      = [tf.identity(v) for v in model_train_vars]
    accumulated_meta_grads = [tf.zeros_like(v) for v in model_train_vars]

    successful_tasks = 0

    for task_id in range(n_tasks):
        # sample per-class adaptation tasks
        episode_tasks = sample_episode(
            per_class_images, rare_classes,
            k_shot_common, k_shot_rare,
            n_query_common, n_query_rare,
            n_way, rare_prob
        )

        if not episode_tasks:
            continue

        for cls_id, support_pairs, query_pairs in episode_tasks:
            # load support/query with class-aware mask
            support_x, support_y = load_images_and_masks(
                support_pairs, is_support=True,
                union_foreground=union_foreground,  # False ➝ 0 / cls_id
                ignore_val=ignore_val
            )
            query_x, query_y = load_images_and_masks(
                query_pairs, is_support=False,
                ignore_val=ignore_val
            )

            if support_x.shape[0] == 0 or query_x.shape[0] == 0:
                continue

            if augment:
                support_x, support_y = augment_images_and_masks(support_x, support_y)
                #query_x,   query_y   = augment_images_and_masks(query_x, query_y)

            # ----- inner loop for this class -----
            task_init_weights = [tf.identity(v) for v in init_meta_weights]

            adapted_w, support_loss = maml_inner_update(
                model, support_x, support_y,
                task_init_weights,
                inner_lr,
                combined_loss_fn,
                n_inner,
                cls_id=cls_id  # for debug
            )
            accumulated_support_loss += float(support_loss)

            # ----- outer loss: query -----
            for var, val in zip(model_train_vars, adapted_w):
                var.assign(val)

            with tf.GradientTape() as outer_tape:
                query_logits = model(query_x, training=False)
                query_loss   = combined_loss_fn(query_y, query_logits)

            grads = outer_tape.gradient(query_loss, model_train_vars)

            # restore meta weights
            for var, val in zip(model_train_vars, init_meta_weights):
                var.assign(val)

            # accumulate
            for i, g in enumerate(grads):
                if g is not None:
                    accumulated_meta_grads[i] += g

            accumulated_meta_loss += float(query_loss)
            successful_tasks      += 1

    # ----------- apply meta-update -----------
    if successful_tasks:
        avg_meta_loss    = accumulated_meta_loss    / successful_tasks
        avg_support_loss = accumulated_support_loss / successful_tasks
        avg_query_loss   = avg_meta_loss

        mean_grads = [g / successful_tasks for g in accumulated_meta_grads]
        clipped_grads, _ = tf.clip_by_global_norm(mean_grads, 1.0)
        meta_optimizer.apply_gradients(zip(clipped_grads, model_train_vars))
    else:
        avg_meta_loss = avg_support_loss = avg_query_loss = 0.0

    return avg_meta_loss, avg_support_loss, avg_query_loss

